        //   A short linear sweep picks up game elements near the close wall.
        currentPhase = "Intake sweep - close floor";
        navigateWithIntake(INTAKEFLOORCLOSE_POSE);
        startReverseFor(OUTTAKE_REVERSE_S);

        // ── 13. Final fire position ────────────────────────────────────────────
