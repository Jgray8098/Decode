        navigateWithIntake(GATELOAD_POSE);
        startReverseFor(OUTTAKE_REVERSE_S);

        currentPhase = "Gate cycle 1 - fire";
        navigate(FIRELOCATION_POSE_3);
        shootFrom(LAUNCH_DISTANCE_MAIN);

        // ── Gate cycle 2 ───────────────────────────────────────────────────────
